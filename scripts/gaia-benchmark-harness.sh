#!/usr/bin/env bash
# gaia-benchmark-harness.sh — SPEC-100 Slice 1 GAIA benchmark harness.
#
# Scaffolding para integrar GAIA (General AI Assistants Benchmark)
# como eval externa de agentes pm-workspace. Emite:
#   - harness-config.yaml    config para runner externo
#   - prompts-subset.jsonl   subset de tareas GAIA seleccionadas
#   - results-template.json  esqueleto del report post-run
#
# NO descarga el dataset GAIA ni ejecuta eval — solo emite scaffolding.
# GAIA dataset (huggingface.co/datasets/gaia-benchmark) tiene ~466 tasks
# con 3 niveles: L1 (basic), L2 (multi-step), L3 (long-horizon).
#
# Usage:
#   gaia-benchmark-harness.sh --level L1 --output-dir output/gaia/
#   gaia-benchmark-harness.sh --level L2 --output-dir o/ --agent-id savia
#   gaia-benchmark-harness.sh --level all --output-dir o/ --n-tasks 30
#
# Exit codes:
#   0 — harness scaffolded
#   2 — usage error
#
# Ref: SPEC-100, ROADMAP §Tier 4.9
# Safety: read-only on repo, write only in --output-dir. set -uo pipefail.

set -uo pipefail

LEVEL=""
OUTPUT_DIR=""
AGENT_ID="default"
N_TASKS=10

VALID_LEVELS="L1 L2 L3 all"

usage() {
  cat <<EOF
Usage:
  $0 --level LEVEL --output-dir DIR [options]

Required:
  --level LEVEL       One of: $VALID_LEVELS
  --output-dir DIR    Destination for harness config + templates

Optional:
  --agent-id NAME     Agent identifier for results (default "default")
  --n-tasks N         Tasks to sample per level (default 10)

Emite scaffolding para GAIA benchmark. NO descarga dataset ni ejecuta.

Next steps (manual):
  1. Download GAIA dataset: huggingface.co/datasets/gaia-benchmark
  2. Load prompts into <output-dir>/prompts-subset.jsonl
  3. Run agent on each prompt, populate results template
  4. Submit to GAIA leaderboard (optional)

Ref: SPEC-100, ROADMAP §Tier 4.9
EOF
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --level) LEVEL="$2"; shift 2 ;;
    --output-dir) OUTPUT_DIR="$2"; shift 2 ;;
    --agent-id) AGENT_ID="$2"; shift 2 ;;
    --n-tasks) N_TASKS="$2"; shift 2 ;;
    -h|--help) usage; exit 0 ;;
    *) echo "ERROR: unknown arg '$1'" >&2; exit 2 ;;
  esac
done

[[ -z "$LEVEL" ]] && { echo "ERROR: --level required" >&2; exit 2; }
[[ -z "$OUTPUT_DIR" ]] && { echo "ERROR: --output-dir required" >&2; exit 2; }

if ! echo " $VALID_LEVELS " | grep -q " $LEVEL "; then
  echo "ERROR: invalid --level '$LEVEL' (allowed: $VALID_LEVELS)" >&2
  exit 2
fi

if ! [[ "$N_TASKS" =~ ^[0-9]+$ ]] || [[ "$N_TASKS" -le 0 ]]; then
  echo "ERROR: --n-tasks must be positive integer" >&2
  exit 2
fi

mkdir -p "$OUTPUT_DIR"

# Determine level list.
LEVELS=()
if [[ "$LEVEL" == "all" ]]; then
  LEVELS=(L1 L2 L3)
else
  LEVELS=("$LEVEL")
fi

# Emit harness-config.yaml.
TS=$(date -u +%Y-%m-%dT%H:%M:%SZ)
CFG="$OUTPUT_DIR/harness-config.yaml"
cat > "$CFG" <<YAML
# GAIA Benchmark Harness Config (SPEC-100)
# Generated by scripts/gaia-benchmark-harness.sh on $TS
# Ref: docs/propuestas/SPEC-100-gaia-benchmark-integration.md

benchmark:
  name: "GAIA"
  dataset: "huggingface.co/datasets/gaia-benchmark"
  levels:
$(for l in "${LEVELS[@]}"; do echo "    - $l"; done)
  n_tasks_per_level: $N_TASKS

agent:
  id: "$AGENT_ID"
  version: "<set-before-run>"

runner:
  timeout_per_task_seconds: 300
  max_retries: 1
  parallel_tasks: 1

scoring:
  metric: "exact_match"      # GAIA uses exact-match for final answer
  include_chain_of_thought: false

sovereignty:
  zero_egress: true           # all eval runs local
  leaderboard_submit: false   # opt-in manual step

output:
  results_path: "$OUTPUT_DIR/results-$AGENT_ID.json"
  log_path: "$OUTPUT_DIR/run.log"
YAML

# Emit prompts-subset.jsonl (placeholder structure, user fills from dataset).
PROMPTS="$OUTPUT_DIR/prompts-subset.jsonl"
cat > "$PROMPTS" <<'EOF'
{"task_id": "gaia-L1-001", "level": "L1", "question": "<placeholder — fill from GAIA dataset>", "expected_answer": "<expected>", "tools_required": ["web_search", "file_read"]}
{"task_id": "gaia-L1-002", "level": "L1", "question": "<placeholder>", "expected_answer": "<expected>", "tools_required": ["calculator"]}
EOF

# Emit results-template.json.
RESULTS="$OUTPUT_DIR/results-template.json"
levels_json=$(printf '"%s",' "${LEVELS[@]}")
levels_json="[${levels_json%,}]"
cat > "$RESULTS" <<JSON
{
  "agent_id": "$AGENT_ID",
  "agent_version": "<set>",
  "benchmark": "GAIA",
  "run_started_at": null,
  "run_completed_at": null,
  "levels_evaluated": $levels_json,
  "tasks": [],
  "summary": {
    "total_tasks": 0,
    "correct": 0,
    "accuracy": null,
    "per_level": {}
  }
}
JSON

echo "gaia-benchmark-harness: scaffolded"
echo "  config:   $CFG"
echo "  prompts:  $PROMPTS (placeholder — fill from GAIA dataset)"
echo "  results:  $RESULTS"
echo "  agent:    $AGENT_ID"
echo "  levels:   ${LEVELS[*]}"
echo ""
echo "Next steps (manual):"
echo "  1. Download GAIA dataset from HuggingFace"
echo "  2. Replace placeholders in $PROMPTS with real tasks"
echo "  3. Run agent: <your-runner> --config $CFG"
echo "  4. Parse results into $RESULTS schema"

exit 0
